`timescale 1ns/1ps

module Immediate_Generation_tb;

    reg  [31:0] instr;
    wire [63:0] imm;

    // DUT
    Immediate_Generation uut (
        .instr(instr),
        .imm(imm)
    );

    initial begin

        $display("---------------------------------------------------");
        $display("Time\tInstruction\t\tImmediate(hex)\tImmediate(dec)");
        $display("---------------------------------------------------");

        // =====================================================
        // I-TYPE : ADDI x1, x2, -5
        // =====================================================
        instr = 32'b111111111011_00010_000_00001_0010011;
        #10;
        $display("%0t\t%h\t%h\t%0d", $time, instr, imm, $signed(imm));

        // =====================================================
        // LOAD : LD x3, 8(x4)
        // =====================================================
        instr = 32'b000000001000_00100_011_00011_0000011;
        #10;
        $display("%0t\t%h\t%h\t%0d", $time, instr, imm, $signed(imm));

        // =====================================================
        // S-TYPE : SD x5, -16(x6)
        // =====================================================
        instr = 32'b1111111_00101_00110_011_10000_0100011;
        #10;
        $display("%0t\t%h\t%h\t%0d", $time, instr, imm, $signed(imm));

        // =====================================================
        // B-TYPE : BEQ x1, x2, +8
        // =====================================================
        instr = 32'b0000000_00010_00001_000_00100_1100011;
        #10;
        $display("%0t\t%h\t%h\t%0d", $time, instr, imm, $signed(imm));

        // =====================================================
        // U-TYPE : LUI x7, 0x12345
        // =====================================================
        instr = 32'b00010010001101000101_00111_0110111;
        #10;
        $display("%0t\t%h\t%h\t%0d", $time, instr, imm, $signed(imm));

        // =====================================================
        // J-TYPE : JAL x1, +16
        // =====================================================
        instr = 32'b00000000100000000000_00001_1101111;
        #10;
        $display("%0t\t%h\t%h\t%0d", $time, instr, imm, $signed(imm));

        $display("---------------------------------------------------");

        $finish;
    end

endmodule

